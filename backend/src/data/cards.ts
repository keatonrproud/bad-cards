// Utility functions for card management
export const shuffleCards = <T>(cards: T[]): T[] => {
  const shuffled = [...cards];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled;
};

export const drawCards = <T>(deck: T[], count: number): T[] => {
  return deck.splice(0, count);
};

export const getRandomWhiteCard = (): WhiteCard => {
  const randomIndex = Math.floor(Math.random() * whiteCards.length);
  return whiteCards[randomIndex];
};

export const dealBlackCards = (count: number): BlackCard[] => {
  const shuffled = shuffleCards([...blackCards]);
  return shuffled.slice(0, count);
};
